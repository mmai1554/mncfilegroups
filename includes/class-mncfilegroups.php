	 * The loader that's responsible for maintaining and registering all hooks that power
	 * the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      Mncfilegroups_Loader $loader Maintains and registers all hooks for the plugin.
	 */
